from heapq import heapify, heappop


class DictQueue(dict):

    def __init__(self, *args, **kwargs):
        super(DictQueue, self).__init__(*args, **kwargs)
        self._rebuild_heap()

    def _rebuild_heap(self):
        self._heap = [(v, k) for k, v in self.iteritems()]
        heapify(self._heap)

    def smallest(self):
        heap = self._heap
        v, k = heap[0]
        while k not in self or self[k] != v:
            heappop(heap)
            v, k = heap[0]
        return v, k

    def pop_smallest(self):
        heap = self._heap
        v, k = heappop(heap)
        while k not in self or self[k] != v:
            v, k = heappop(heap)
        del self[k]
        return (v, k)

    def __setitem__(self, key, val):
        super(DictQueue, self).__setitem__(key, val)
        self._rebuild_heap()

    def setdefault(self, key, val):
        if key not in self:
            self[key] = val
            return val
        return self[key]

    def update(self, *args, **kwargs):
        super(DictQueue, self).update(*args, **kwargs)
        self._rebuild_heap()

    def sorted_iter(self):
        while self:
            yield self.pop_smallest()

    def empty(self):
        return len(self) == 0

    def queue(self):
        new_list = [(t[1], t[0]) for t in self.items()]
        return sorted(new_list, key=lambda t: t[0])
